# 걸리는 시간 출력
# 입력받은 문자를 딕셔너리에 추가해서 해당하는 알파벳의 넘버가 걸리는 시간을 부여
# 예를 들어 a : 3, b : 3, c: 3, d : 4

num = input()

time = {
    'A' : 3, 'B' : 3, 'C' : 3,
    'D' : 4, 'E' : 4, 'F' : 4,
    'G' : 5, 'H' : 5, 'I' : 5,
    'J' : 6, 'K' : 6, 'L' : 6,
    'M' : 7, 'N' : 7, 'O' : 7,
    'P' : 8, 'Q' : 8, 'R' : 8, 'S' : 8,
    'T' : 9, 'U' : 9, 'V' : 9,
    'W' : 10, 'X' : 10, 'Y' : 10, 'Z' : 10,
}

sum_ = 0
for i in num:
    sum_ += time[i]
print(sum_)

